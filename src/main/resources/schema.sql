-- テーブル削除
DROP TABLE IF EXISTS items;
DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS orders;
DROP TABLE IF EXISTS order_details;
DROP TABLE IF EXISTS genres;


-- 商品テーブル
CREATE TABLE items
(
   id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   name TEXT,
   price INTEGER,
   image TEXT,
   genre INTEGER
);
-- 顧客テーブル
CREATE TABLE users
(
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name TEXT,
	address TEXT,
	password TEXT,
	email TEXT
);
-- 注文テーブル
CREATE TABLE orders
(
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	user_id INTEGER,
	total_price INTEGER 
);
-- 注文明細テーブル
CREATE TABLE order_details
(
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	order_id INTEGER,
	item_id INTEGER,
	quantity INTEGER
);
-- ジャンルテーブル
CREATE TABLE genres
(
	id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	name TEXT
);
